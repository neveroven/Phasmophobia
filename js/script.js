const NameGhost = document.getElementById("NameGhost")
const DisGhost = document.getElementById("DisGhost")
const StrengthGhost = document.getElementById("StrengthGhost")
const WeaknessGhost = document.getElementById("WeaknessGhost")
const EvidenceGhost = document.getElementById("EvidenceGhost")
const EvidenceList = document.getElementById("EvidenceList")

function SelectGhost (index)
{
    const Ghost = Ghosts[index]
    NameGhost.textContent = Ghost.Name
    DisGhost.textContent = Ghost.Discription
    StrengthGhost.textContent = Ghost.Strength
    WeaknessGhost.textContent = Ghost.Weakness
    EvidenceGhost.textContent = "Evidence"
    EvidenceList.innerHTML = ""
    Ghost.Evidence.forEach(e=>{
        const li = document.createElement("li")
        li.textContent = e
        EvidenceList.appendChild(li)
    })
}